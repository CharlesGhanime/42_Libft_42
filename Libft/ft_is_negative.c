int		ft_putchar(char c);

void	ft_is_negative(int n)
{
	char c;

	c = 0;
	if (n >= 0)
	{
		c = 80;
		ft_putchar(c);
	}
	else
	{
		c = 78;
		ft_putchar(c);
	}
}
